-- ============================================================
-- 2. pregnancy_weeks 테이블 생성
-- ============================================================

CREATE TABLE IF NOT EXISTS pregnancy_weeks (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  week INTEGER NOT NULL UNIQUE CHECK (week >= 1 AND week <= 42),
  trimester INTEGER GENERATED ALWAYS AS (
    CASE
      WHEN week <= 13 THEN 1
      WHEN week <= 27 THEN 2
      ELSE 3
    END
  ) STORED,
  baby_size_fruit VARCHAR(50),
  baby_size_cm DECIMAL(5,1),
  baby_weight_g DECIMAL(7,1),
  baby_development TEXT NOT NULL,
  mom_changes TEXT NOT NULL,
  dad_tips TEXT NOT NULL,
  tips TEXT,
  warnings TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_pregnancy_weeks_week ON pregnancy_weeks(week);

ALTER TABLE pregnancy_weeks ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Authenticated users can read pregnancy_weeks" ON pregnancy_weeks FOR SELECT USING (auth.role() = 'authenticated');

COMMENT ON TABLE pregnancy_weeks IS '주수별 태아 성장 정보 (1~42주)';

